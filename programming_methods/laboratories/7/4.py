"""
Задача №100. Строчки

Мальчик Кирилл написал однажды на листе бумаги строчку, состоящую из больших и маленьких латинских букв,
а после этого ушел играть в футбол.
Когда он вернулся, то обнаружил, что его друг Дима написал под его строкой еще одну строчку такой же длины.
Дима утверждает, что свою строчку он получил циклическим сдвигом строки Кирилла на несколько шагов вправо
(циклический сдвиг строки abcde на 2 позиции вправо даст строку deabc).
Однако Дима известен тем, что может случайно ошибиться в большом количестве вычислений, поэтому Кирилл в растерянности – верить ли Диме?
Помогите ему! По данным строкам выведите минимальный возможный размер сдвига или -1, если Дима ошибся.

Входные данные

Первые две строки входных данных содержат строки Кирилла и Димы, соответственно. Длины строк одинаковы, не превышают 10000 и не равны 0.

Выходные данные

Выведите единственное число – ответ на вопрос задачи.
"""


def minimal_cyclic_shift(kirill: str, dima: str) -> int:
    # Если строки совпадают, сдвиг равен 0
    if kirill == dima:
        return 0

    # Удваиваем строку Димы для поиска циклического сдвига
    doubled_dima = dima * 2
    shift_index = doubled_dima.find(kirill)

    # Если Кириллову строку не удалось найти, значит, Дима ошибся
    return -1 if shift_index == -1 else shift_index


def main() -> None:
    kirill_string = input()
    dima_string = input()

    result = minimal_cyclic_shift(kirill_string, dima_string)
    print(result)


if __name__ == "__main__":
    main()
